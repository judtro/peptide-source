export const getArticleBySlug = (slug: string): Article | undefined => {
  return articles.find((article) => article.slug === slug);
};

export const getArticlesByCategory = (category: Article['category']): Article[] => {
  return articles.filter((article) => article.category === category);
};

export const getRelatedArticles = (currentSlug: string, limit: number = 3): Article[] => {
  const current = getArticleBySlug(currentSlug);
  if (!current) return articles.slice(0, limit);
  
  return articles
    .filter((article) => article.slug !== currentSlug)
    .sort((a, b) => {
      // Prioritize same category
      const aMatch = a.category === current.category ? 1 : 0;
      const bMatch = b.category === current.category ? 1 : 0;
      return bMatch - aMatch;
    })
    .slice(0, limit);
};

export const getArticleCountByCategory = (category: Article['category']): number => {
  return articles.filter((article) => article.category === category).length;
};
